stash_hrsprites:

            +STASHI hrsprite_data, REU_ADDR_HRSPRITES, hrsprite_end-hrsprite_data

            +PHW $FB

            SEI

            LDA $01
            PHA
            AND #$F8
            STA $01

            +STIW $FB, hrsprite_data
            +STIW $FD, $D000

            LDY #0

-           LDA $FB
            CMP #<hrsprite_end
            BNE +
            LDA $FC
            CMP #>hrsprite_end
            BEQ ++

+           LDA ($FB),Y
            STA ($FD),Y
            +ADDW $FB, 1
            +ADDW $FD, 1
            JMP -

++          PLA
            STA $01

            CLI

            +PLW $FB

            RTS

